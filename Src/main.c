/* USER CODE BEGIN 4 */
void UartDMAGet()
{
	if(recv_end_flag ==1)
	{
			/*        打印接收到的数据长度 */
			printf("\nrx_len=%d\r\n",Rx_len);

			for(int i=0;i<Rx_len;i++)
			{
				/*        打印接收到的数据 */
				printf("%c",ReceiveBuff[i]);
			}
			printf("\r\n");          
			/*        清空接收缓存区        */
			for(int i = 0; i < Rx_len ; i++) 
				ReceiveBuff[i]=0;
			/*        接收数据长度清零        */
			Rx_len=0;
			recv_end_flag=0;
	}
	/*     开启下一次接收        */
	HAL_UART_Receive_DMA(&huart1,(uint8_t*)ReceiveBuff,BUFFERSIZE);	
}	
